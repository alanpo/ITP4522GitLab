public class Employee {
	private String name;   
	private int salary;    

	public void setName(String name) { 
		this.name = name; 
	}    

	public void setSalary(int salary) { 
		this.salary = salary; 
	}

	public String getName() { 
		return this.name; 
	}

	public int getSalary() { 
		return this.salary; 
	}
}
